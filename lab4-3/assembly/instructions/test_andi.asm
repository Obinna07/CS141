# tests for the and-immediate instruction

addi $t0, $0, 10                  # register t0 now holds integer 10:
                              # (00000000000000000000000000001010)

                              # (00000000000000000000000000001010)
                              # (00000000000000000000000000001011)
andi $t0, $t0, 11                 # register t0 still holds integer 10:
                              # (00000000000000000000000000001010)

                              # (00000000000000000000000000001010)
                              # (00000000000000000000000000001101)
andi $t0, $t0, 13                 # register t0 now holds integer 8:
                              # (00000000000000000000000000001000)

                              # (00000000000000000000000000001000)
                              # (11111111111111111111111111111111)
andi $t0, $t0, -1                 # register t0 still holds integer 8:
                              # (00000000000000000000000000001000)

                              # (00000000000000000000000000001000)
                              # (00000000000000000000000000000000)
andi $t0, $t0, 0                  # register t0 now holds all 0s:
                              # (00000000000000000000000000000000)
